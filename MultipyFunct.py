#This file is used as a module

#defining multiply function
def mult_num(a,b):
  print("Result of %d * %d is %d" %(a,b,a*b)) 
  return;

#defining addition function
def add_num(a,b):
 print("Result of %d + %d is %d" %(a,b,a+b)) 
 return;

#defining subtract function
def sub_num(a,b):
 print("Result of %d - %d is %d" %(a,b,a-b)) 
 return;

#defining division function
def div_num(a,b):
 print("Result of %d / %d is %d" %(a,b,a/b)) 
 return;

#defining modulus function
def mod_num(a,b):
 print("Result of %d %% %d is %d" %(a,b,a%b)) 
 return;